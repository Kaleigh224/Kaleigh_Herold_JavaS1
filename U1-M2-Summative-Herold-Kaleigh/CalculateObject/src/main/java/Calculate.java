public class Calculate {

    public int intAddition(int num1, int num2) {
      return num1 + num2;
    }

    public int intSubtraction(int num1, int num2) {
        return num1 - num2;
    }

    public int intMultiplication(int num1, int num2) {
        return num1 * num2;
    }

    public int intDivision(int num1, int num2) {
        return num1 / num2;
    }

    public double doubleAddition(double num1, double num2) {
        return num1 + num2;
    }

    public double doubleSubtraction(double num1, double num2) {
        return num1 - num2;
    }

    public double doubleMultiplication(double num1, double num2) {
        return num1 * num2;
    }

    public double doubleDivision(double num1, double num2) {
        return num1 / num2;
    }


}
